#include <sys/cdefs.h>
#include <sys/systm.h>

#include "lpc43xx_scu.h"

#define	RD4(_sc, _reg)		\
	*(volatile uint32_t *)((_sc)->base + _reg)
#define	WR4(_sc, _reg, _val)	\
	*(volatile uint32_t *)((_sc)->base + _reg) = _val

void
lpc43xx_scu_init(struct scu_softc *sc, uint32_t base,
    const struct scu_pin *pins, uint8_t npins)
{
	int i;

	sc->base = base;

	for (i = 0; i < npins; i++)
		WR4(sc, SCU_SFSP(pins[i].port, pins[i].pin),
		    pins[i].config);
}
